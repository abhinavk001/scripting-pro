#!/bin/bash
echo -e "

                                                                    Displaying inforamtion realated to this computer
       
       "
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------------------------"
echo -e "OS:"
lsb_release -a
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------------------------"

echo -e "Available Shells: "
echo -e "`cat /etc/shells`"
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------------------------"

echo -e "Computer CPU Information:" 
lscpu
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------------------------"

echo -e "Memory Information:"
free -m
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------------------------"

echo -e "Secondary storage information:"
sudo parted -l
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------------------------"

echo -e "File System (Mount):" 
mount | column -t
echo -e "------------------------------------------------------------------------------------------------------------------------------------------------------------------"